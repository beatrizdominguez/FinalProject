public class MyColor {

    String color;
    String code;

    public MyColor() {

    }

    public MyColor(String color, String code) {
        this.color = color;
        this.code = code;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }
}
